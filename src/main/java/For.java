public class For {
    public static void main(String[] args) {
        for ( int i =0 ; i <=10; i++) { // внутри фор разбиваем на 3 Блока : (1;2;3) после фигурная скобка на начала цикла
            // 1 - создаем переменную для счетчика, 2 до какого момента выполняем цикл. 3 что делаем после выполнения цикла
            System.out.println(i);
        }
    }
}
